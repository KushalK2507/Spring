package com.kushal.SpringCore.referenceTypes;

public class Student {

	private String name;
	private Score scores;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Score getScores() {
		return scores;
	}

	public void setScores(Score scores) {
		this.scores = scores;
	}

}
